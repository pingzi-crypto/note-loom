function resolveLocale(language: PluginLanguage): Locale {
  if (language === "zh" || language === "en") {
    return language;
  }

  if (typeof document !== "undefined") {
    const htmlLanguage = document.documentElement.lang?.toLowerCase();
    if (htmlLanguage?.startsWith("zh")) {
      return "zh";
    }

    if (htmlLanguage?.startsWith("en")) {
      return "en";
    }
  }

  if (typeof window !== "undefined") {
    const savedLanguage = window.localStorage.getItem("language")?.toLowerCase();
    if (savedLanguage?.startsWith("zh")) {
      return "zh";
    }

    if (savedLanguage?.startsWith("en")) {
      return "en";
    }
  }

  const systemLanguage =
    typeof window !== "undefined" ? window.navigator.language.toLowerCase() : "en";
  return systemLanguage.startsWith("zh") ? "zh" : "en";
}

export function t(
  language: PluginLanguage,
  key: string,
  vars?: Record<string, string | number>
): string {
  const locale = resolveLocale(language);
  const dict = locale === "zh" ? zh : en;
  const template = dict[key] ?? key;

  if (!vars) {
    return template;
  }

  return Object.entries(vars).reduce(
    (result, [varKey, value]) => result.split(`{${varKey}}`).join(String(value)),
    template
  );
}
